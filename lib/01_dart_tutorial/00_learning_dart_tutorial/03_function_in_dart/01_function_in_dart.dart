// Function In Dart
// Dalam tutorial ini, Anda akan mempelajari fungsi dalam DART. 
// Fungsi adalah blok kode yang melakukan tugas tertentu. 
// Fungsi dibuat ketika beberapa pernyataan muncul berulang kali dalam program. 
// Fungsi membantu penggunaan kembali kode dalam program.
// Syntax
// returntype functionName(parameter1,parameter2, ...){
//   // function body
// }
// Return type: Ini memberi tahu Anda tipe keluaran fungsi. Bisa berupa void, String, int, double, dll. 
// Jika fungsi tidak mengembalikan apa pun, Anda dapat menggunakan void sebagai Return type.

// Nama Fungsi: Anda dapat memberi nama fungsi dengan hampir semua nama. 
// Selalu ikuti konvensi penamaan lowerCamelCase seperti void printName().

// Parameter: Parameter adalah input ke fungsi, yang dapat Anda tulis di dalam tanda kurung (). 
// Selalu ikuti konvensi penamaan lowerCamelCase untuk parameter fungsi Anda.